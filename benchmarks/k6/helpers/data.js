import { ASSIGNEE_ID } from '../config.js';

const categories = ['SYSTEM', 'NETWORK', 'INFRASTRUCTURE', 'ACCESS', 'DATA', 'PROCESS'];
const priorities = ['LOW', 'MEDIUM', 'HIGH', 'CRITICAL'];

export function createIncidentPayload(overrides = {}) {
  const suffix = uniqueSuffix();

  return {
    title: overrides.title || `k6 benchmark incident ${suffix}`,
    description:
      overrides.description ||
      `Incident generated by k6 benchmark run ${suffix} with enough detail for validation.`,
    category: overrides.category || pick(categories),
    priority: overrides.priority || pick(priorities),
    assigneeId: overrides.assigneeId || ASSIGNEE_ID,
  };
}

export function updateIncidentPayload(overrides = {}) {
  const suffix = uniqueSuffix();

  return {
    title: overrides.title || `k6 updated incident ${suffix}`,
    description:
      overrides.description ||
      `Incident updated by k6 benchmark run ${suffix} with enough detail for validation.`,
    priority: overrides.priority || pick(priorities),
    status: overrides.status || 'IN_PROGRESS',
  };
}

export function pick(values) {
  return values[Math.floor(Math.random() * values.length)];
}

export function randomInt(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function uniqueSuffix() {
  return `${__VU}-${__ITER}-${Date.now()}-${randomInt(1000, 9999)}`;
}
